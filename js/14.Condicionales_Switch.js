let dia = 5;

switch (dia) {
  case 0:
    console.log("Lunes");
    break;
  case 1:
    console.log("Martes");
    break;
  case 2:
    console.log("Miercoles");
    break;
  case 3:
    console.log("jueves");
    break;
  case 4:
    console.log("viernes");
    break;
  case 5:
    console.log("sabado");
    break;
  default:
    console.log("descanso");
    break;
}
